USE `test`;

CREATE TABLE `PERSON` (
  `ID` bigint NOT NULL AUTO_INCREMENT,
  `FIRST_NAME` varchar(100) NOT NULL,
  `LAST_NAME` varchar(100) NOT NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE `DOCUMENT` (
  `ID` bigint NOT NULL AUTO_INCREMENT,
  `CONTENT` text CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL,
  `CREATED_DATE` datetime NOT NULL,
  `DOCUMENT_TYPE` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL,
  `DOCUMENT_NUMBER` varchar(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL,
  `IS_ACTIVE` tinyint(1) NOT NULL,
  `PERSON_ID` bigint NOT NULL,
  PRIMARY KEY (`ID`),
  KEY `DOCUMENT_FK` (`PERSON_ID`),
  CONSTRAINT `DOCUMENT_FK` FOREIGN KEY (`PERSON_ID`) REFERENCES `PERSON` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

